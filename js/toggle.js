$('#toggle_message').click(function(){
	var value = $('#toggle_message').attr('value');
	$('#message').toggle('slow');
	
	if(value == 'DEVELOPER'){
		$('#toggle_message').attr('value','DEVELOPER');	
	}
			else if(value == 'Show'){
		$('#toggle_message').attr('value','Hide');	
		}	
});


